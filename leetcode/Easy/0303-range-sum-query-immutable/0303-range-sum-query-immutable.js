/**
 * @param {number[]} nums
 */
var NumArray = function(nums) {
    this.nums = nums;
};

/** 
 * @param {number} left 
 * @param {number} right
 * @return {number}
 */
NumArray.prototype.sumRange = function(left, right) {
    let i = 0;
    let sum = 0;

    while (left + i <= right) {
        sum += this.nums[left + i]
        i++;
    }
    return sum;
};

